print("Easy program to divide two numbers")
# Use int to convert user inputs into numbers
first_number = int(input("Please enter the first number:"))
second_number = int(input("Please enter the second number:"))
# Use int to keep the result as an integer
division = int(first_number / second_number)
# Use str to convert int into a string and display it along with another string value
print("The division of the first number and the second number is: " + str(division))
